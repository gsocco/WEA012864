//==============================================================
//      initial SSD1306BZ
//==============================================================
void Initial_SSD1306BZ()
{
	DC = 0;
    Write_command(0xAE);			// Display Off

	Write_command(0xD5);  			// SET DISPLAY CLOCK
    Write_command(0x80);  			// 105HZ

    Write_command(0xA8);  			// Select Multiplex Ratio
    Write_command(Duty);  			// Default => 0~0x3F (1/64 Duty)

	Write_command(0xD3);  			// Setting Display Offset
    Write_command(0x00);  			// 00H Reset,  set common start

	Write_command(0x40);  			// Set Display Start Line

	Write_command(0x8D);  			// Set Charge Pump
	Write_command(0x14);  			// Endable Charge Pump

	Write_command(0xA1);  			// Set Segment Re-Map Default
									// 0xA0 (0x00) => column Address 0 mapped to 127
                            		// 0xA1 (0x01) => Column Address 127 mapped to 0

	Write_command(0xAD);  			// Set Charge Pump
	Write_command(0x30);  			// Endable Charge Pump

	Write_command(0xC8);  			// Set COM Output Scan Direction
									// 0xC0	(0x00) => normal mode (RESET) Scan from COM0 to COM[N-1];Where N is the Multiplex ratio.
									// 0xC8	(0xC8) => remapped mode. Scan from COM[N-1] to COM0;;Where N is the Multiplex ratio.

	Write_command(0xDA);  			// Set COM Hardware Configuration
    Write_command(0x12);  			// Alternative COM Pin---See IC Spec page 34
									//(0x02)=> A4=0;Sequential COM pin configuration;A5=0;Disable COM Left/Right remap

	Write_command(0x81);  			// Set Contrast Control
	Write_command(Contrast_volue);

	Write_command(0xD9);  			// Set Pre-Charge period
    Write_command(0x22);

	Write_command(0xDB);  			// Set Deselect Vcomh level
    Write_command(0x30);

    Write_command(0xA4);  			// Entire Display ON

	Write_command(0xA6);  			// Set Normal Display
    Write_command(0xAF);  			// Display ON
}

//-------------------------------------------------
//          Write_byte
//-------------------------------------------------
// Escribe un byte en el display
void Write_byte(unsigned char dat)
{
	unsigned char i;
    for(i=0;i<8;i++){
    	SCL=0;
		if((dat<<i)&0x80){
		  SDA=1;
		}else{
		  SDA=0;
		}
		SCL=1;
		SCL=0;
    }
    SDA=1;
    SCL=1;
    while(1==SDA){}
    SCL=0;
}

//-------------------------------------------------
//          Write Data Function
//-------------------------------------------------
// Escribe datos en el display
void Write_data(unsigned char datap){
	unsigned char bMask;

	CS = 0;
	DC = 1;

	for(bMask = 0x80; bMask; bMask >>= 1){
		SCL = 0;
		SDA = bMask & datap;
		SCL = 1;
	}
	CS = 1;

	CS1 = 0;
	DC = 1;

	for(bMask = 0x80; bMask; bMask >>= 1){
		SCL = 0;
		SDA = bMask & datap;
		SCL = 1;
	}
	CS1 = 1;

	CS0 = 0;
	DC = 1;

	for(bMask = 0x80; bMask; bMask >>= 1){
		SCL = 0;
		SDA = bMask & datap;
		SCL = 1;
	}
	CS0 = 1;
}
